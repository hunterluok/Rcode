gsection<-function(ftn,x.l,x.r,x.m,tol=1e-9){
grl<-1+(1+sqrt(5))/2
f.l<-ftn(x.l)
f.r<-ftn(x.r)
f.m<-ftn(x.m)
while((x.r-x.l)>tol){
if((x.r-x.m)>(x.m-x.l)){
y<-x.m+(x.r-x.m)/grl
f.y<-ftn(y)
if(f.y>=f.m){
x.1<-x.m
f.l<-f.m
x.m<-y
f.m<-f.y
}else{
x.r<-y
f.r<-f.y}
}else{
y<-x.m-(x.m-x.l)/grl
f.y<-ftn(y)
if(f.y>=f.m){
x.r<-x.m
f.r<-f.m
x.m<-y
f.m<-f.y
}else{
x.l<-y
f.l<-f.y
}
}
}
return(x.m)
}
